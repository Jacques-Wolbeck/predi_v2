class Related {
  String? _code;
  String? _id;
  String? _extension;
  String? _version;

  Related();

  void setCode(String code) => _code = code;

  String? getCode() => _code;

  void setID(String id) => _id = id;

  String? getId() => _id;

  void setExtension(String extenSion) => _extension = extenSion;

  String? getExtension() => _extension;

  void setVersion(String version) => _version = version;

  String? getVersion() => _version;
}
